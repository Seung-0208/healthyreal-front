<script setup>
import { defineEmits, defineProps, onUpdated } from "vue"

const props = defineProps({
  isDialogVisible: {
    type: Boolean,
    required: true,
  }, 
  message: {
    type: String,
    required: true,
  },
})

const emit = defineEmits([
  'submit',
  'update:isDialogVisible',
])


const warningReason = ref('')

onUpdated(()=>{
  const inputTags = document.querySelectorAll('[name = warningReport]')

  inputTags.forEach(ele => {ele.checked = !ele.checked})
})


const reportController = e => { //axios로 처리
  console.log(e.target)
  warningReason.value = e.target.value
}

const clickEvt = ()=>{
  emit('update:isDialogVisible', false)
  emit('checkConfirm', warningReason, props.message)
}
</script>

<template>
  <VDialog
    :width="$vuetify.display.smAndDown ? 'auto' : 600"
    :model-value="props.isDialogVisible"
    @update:model-value="val => $emit('update:isDialogVisible', val)"
  >
    <VCard class="pa-5 pa-sm-8">
      <!-- 👉 dialog close btn -->
      <DialogCloseBtn
        variant="text"
        size="small"
        @click="$emit('update:isDialogVisible', false)"
      />

      <!-- 👉 Title -->
      <VCardItem class="text-center">
        <VCardTitle class="text-h5 mb-3">
          {{ props.message }}님을 위험 메이트로 신고합니다
        </VCardTitle>
        <VRadioGroup
          id="warningReport"
          :style="{'padding-left':'15px'}"
          color="error"
          @change="reportController"
        >
          <VRadio
            label="명의 도용"
            value="illegalName"
          />
          <VRadio
            label="폭력적 위협"
            value="violence"
          />
          <VRadio
            label="스팸 및 사기"
            value="spam"
          />
          <VRadio
            label="사생활 침해"
            value="privacy"
          />
        </VRadioGroup>
      </VCardItem>
      <VCol
        cols="12"
        class="d-flex flex-wrap justify-center gap-4"
      >
        <VBtn
          type="submit"
          @click="clickEvt"
        >
          YES
        </VBtn>
        <VBtn
          color="secondary"
          variant="tonal"
          @click="$emit('update:isDialogVisible', false)"
        >
          NO
        </VBtn>
      </VCol>
    </VCard>
  </VDialog>
</template>
